class Solution:
    
    #Function to find starting point where the truck can start to get through
    #the complete circle without exhausting its petrol in between.
    def tour(self,lis, n):
        totalpetrol=0
        totaldistance=0
        for i in lis:
            totalpetrol+=i[0]
            totaldistance+=i[1]
        
        # distance more than petrol travel not possible    
        if(totaldistance>totalpetrol):
            return -1
            
        ans=0    
        max2=0  # can be treated as petrol in tank
        # kadane's algo modification
        for i in range(n):
            max2+=(lis[i][0]-lis[i][1])
                
            if(max2<0):
                ans=i+1 # starting point will be next index
                max2=0  # start with empty tank
        return ans
            
